package Operators_Assignment;

public class Q_5 {
    public static void main(String[] args) {
        int x = 6;
        int y = 7;

        System.out.println(x & y);
        System.out.println(x | y);
        System.out.println(x ^ y);
        System.out.println(x << 1);
        System.out.println(x >> 1);

    }
}
